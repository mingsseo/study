// 1. Main은 클래스의 이름이다.
// 2. 클래스는 뭘까? 하나의 파일이다.
// 3. 클래스는 외부나 내부에서 생성할 수 있다.
public class Main {

    // 1. Method = Function = 함수
    // 2. static 처음 클래스가 생성될 때, 같이 생성됨.
    // 3. void - 반환되는게 없다.
    // 4. main - 함수의 이름
    // 5. args - 입력 값, String[] 입력 값의 타입
    // 6. 실행할 때, main이 꼭 필요함.
    public static void main(String[] args) {
        // 1. System.out.println = 출력하는 방법 중 하나 () 괄호 사이에 출력하려는 문자열을 넣으면 됨.
        System.out.println("Hello world!");
    }

}